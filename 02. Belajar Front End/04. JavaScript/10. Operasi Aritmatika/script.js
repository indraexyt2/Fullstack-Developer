// % <- modulo
// ++ <- increment
// -- <- decrement
// ** pangkat atau power

let x = 10;
console.log(x + 1);
console.log(x - 1);
console.log(x / 2);
console.log(x * 2);

// aritmatika khusus javascript
console.log(x % 4) // sisa pembagian
console.log(x ** 2) // pangkat

// pemrograman biasa
x = x + 1;
console.log(x);
x = x + 1;
console.log(x);
x ++;
console.log(x);
x ++;
console.log(x);